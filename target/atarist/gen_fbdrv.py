#!/usr/bin/env python3
"""
gen_fbdrv.py -- Emit target/atarist/src/fbdrv.s.

Output goes to stdout. target/atarist/build.sh pipes it into src/fbdrv.s
before invoking stcmd make.

History notes for the next person who reads this:

  The first pass at this generator emitted a fully-unrolled MOVEM block
  (500 iterations of `movem.l <abs>.l, d0-d7/a0-a7` + `movem.l d0-d7/
  a0-a7, d16(a3)` -- 16 bytes per iteration, ~8 KB total). It is the
  fastest possible m68k cart->ST screen copy AND it crashed the m68k
  with four bombs at boot. Reason: the load-side `movem.l` includes
  every register, which means a3 gets clobbered with whatever the cart
  FB had at offset 44 -- and the very next instruction tries to use
  a3 as the destination base pointer.

  md-sprites-demo dodges this by using absolute long addressing for
  *both* source AND destination, then generating one MOVEM stream per
  ST screen page (= 16 KB of code for double-buffering). That doesn't
  fit our cart budget alongside main.s / userfw.s / fbdrv_ste.s.

  So this version emits a small `movem`-based LOOP instead: d7 holds
  the iteration counter, d0-d6 form the load list (a3 and a4 stay out
  so they remain valid as pointers), a4 advances the source via
  post-increment, a3 advances the destination via `lea`. About 38
  bytes of code, ~11 ms per 32 KB copy on 8 MHz -- fits inside a
  20 ms VBL with margin to spare. The Q3 plan was "unrolled MOVEM";
  this is the loop fallback we ended up needing.
"""

import sys

FB_SRC_ADDR = 0x00FA8300       # FRAMEBUFFER_ADDR in main.s
FB_SIZE     = 32000            # 320 x 200 4bpp
ITER_BYTES  = 28               # 7 data regs * 4 bytes (d7 is the loop counter)

main_iters  = FB_SIZE // ITER_BYTES        # 1142
main_bytes  = main_iters * ITER_BYTES       # 31976
tail_bytes  = FB_SIZE - main_bytes          # 24
tail_longs  = tail_bytes // 4               # 6

assert tail_bytes % 4 == 0, "tail must be a multiple of 4"
assert tail_longs <= 7,    "tail must fit in d0-d6"

emit = sys.stdout.write

emit("; fbdrv.s -- generated by target/atarist/gen_fbdrv.py\n")
emit(";\n")
emit("; Copies the cartridge framebuffer (FRAMEBUFFER_ADDR = $00FA8300,\n")
emit(";   FRAMEBUFFER_SIZE = {} bytes) into a caller-supplied ST screen\n".format(FB_SIZE))
emit("; page passed in A3.\n")
emit(";\n")
emit("; Caller protocol:\n")
emit(";   move.l  #st_screen_addr, a3   ; SCREEN_A or SCREEN_B\n")
emit(";   jsr     fbdrv\n")
emit(";\n")
emit("; Clobbers: D0-D7, A3 (advanced past end-of-screen), A4. Preserves\n")
emit("; A0-A2, A5-A7.\n")
emit(";\n")
emit("; This is the *loop* form, not the fully unrolled MOVEM block we\n")
emit("; originally tried -- the unrolled version put a3 in the load list\n")
emit("; and bus-errored on the very first iteration. See gen_fbdrv.py's\n")
emit("; header for the full story.\n")
emit(";\n")
emit("; Iteration count: {}   Main bytes: {}   Tail bytes: {}\n".format(
    main_iters, main_bytes, tail_bytes))
emit(";\n")
emit("; DO NOT HAND-EDIT. Regenerate via gen_fbdrv.py.\n")
emit("\n")
emit("    section text\n")
emit("\n")
emit("fbdrv:\n")
emit("    movea.l #${:08X}, a4                ; source pointer (post-inc)\n".format(FB_SRC_ADDR))
emit("    move.l  #({} - 1), d7                ; loop counter\n".format(main_iters))
emit(".loop:\n")
emit("    movem.l (a4)+, d0-d6                  ; load 28 bytes; advance a4\n")
emit("    movem.l d0-d6, (a3)                   ; store 28 bytes at a3\n")
emit("    lea     {}(a3), a3                    ; advance a3 by 28\n".format(ITER_BYTES))
emit("    dbf     d7, .loop\n")
emit("    ; tail: copy the final {} bytes ({} longwords) that don't fit\n".format(
    tail_bytes, tail_longs))
emit("    ; in the {}-byte main iteration.\n".format(ITER_BYTES))

tail_regs = "d0-d{}".format(tail_longs - 1) if tail_longs > 1 else "d0"
emit("    movem.l (a4)+, {}\n".format(tail_regs))
emit("    movem.l {}, (a3)\n".format(tail_regs))
emit("    rts\n")
